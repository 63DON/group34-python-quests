def calculate_area(length, width):
    area = length * width
    return area

area1 = calculate_area(8, 5)
print(f"Rectangle 1 (8 x 5) -> Area = {area1} square units")
area2 = calculate_area(12.5, 4.2)
print(f"Rectangle 2 (12.5 x 4.2) -> Area = {area2} square units")
